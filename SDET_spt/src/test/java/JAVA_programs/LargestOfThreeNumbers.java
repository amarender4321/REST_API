public class LargestOfThreeNumbers {

	public static void main(String[] args) {

		
		int a=100,b=200,c=300;
		if(a>b && a>c) {
			System.out.println("a is bigest: "+a);
		}
		else if(b>a && b>c) {
			System.out.println("b is bigest :"+b);
		}
		else {
			System.out.println("c is bigest :"+c);
		}

	}

}
