package PracticeFolder;

public class Demo {
	
private	 String name;
private     int age;
private	 String email;


public Demo(int age, String name, String email) {
	
	this.name=name;
	this.age=age;
	this.email=email;
}


public String getName(){

return this.name;
}
public void setName(){

name=name;

}

public int getAge(){

return this.age;
}
public void setAge(){

age=age;
}


public String getEmail(){

return this.email;
}
public void setEmail(){

name=email;

}


}
